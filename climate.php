<h2 id="sectionTitle">Climate</h2>

<p id="sectionContent">Maximum temperatures during the winter months are usually between 10°C and 16°C. Although it is
    rare, snow can fall in Izmir from December to February staying for a period of hours rather than a whole day or
    more. During summer, the air temperature can climb as high as 40°C (104°F) from June to September; however it is
    usually between 30°C and 36°C.</p>

