<h2 id="sectionTitle">History</h2>

<p id="sectionContent">The ancient city was known as Smyrna. Izmir has almost 4,000 years of recorded urban history.
    Ancient Greek, Lydian, Persian, Alexander the Great, Roman, Byzantine Empire, Selcuk Turks and Ottoman Empire
    conquered and managed the city.</p>

